namespace Util
{
    /// <summary>
    /// Floating-point rect
    /// </summary>
    public class FRect
    {
        // ================================================================================
        // Constructors
        // ================================================================================
        public FRect(float x, float y, float width, float height)
        {
            X = x;
            Y = y;
            Width = width;
            Height = height;
        }

        // ================================================================================
        // Variables
        // ================================================================================

        // ================================================================================
        // Properties
        // ================================================================================
        public float X { get; private set; }
        public float Y { get; private set; }
        public float Width { get; private set; }
        public float Height { get; private set; }


        // ================================================================================
        // Public methods
        // ================================================================================

        // ================================================================================
        // Private methods
        // ================================================================================
    }
}
